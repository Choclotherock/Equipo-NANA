# El siguiente código realiza las 4 operaciones basicas con dos numeros dados su finalidad es mostrar los
# resultados al usuario 
numero1 = 5  #Asigna un valor a una variable con ayuda del signo igual
numero2 = 3  #Igual asignamos valor a la variable

# Con el fin de trabajar comodamente con las cantidades se guardan en variables con nombres fáciles de recordar para el que las maneje 

suma = numero1 + numero2 #Guarda en la variable llamada "suma" la operación sumatoria de las primeras variables 
resta = numero1 - numero2 #Guarda en la variable llamada "resta" la operación resta de las primeras variables    
multiplicacion = numero1 * numero2 #Guarda la variable llamada "multiplicacion" esta operación multiplicara las variables asignadas
division = numero1 / numero2 #Guarda la variable llamada "division" y dividira las variables asignadas

#Ahora en esta seccion se va a seleccionar que operacion se quiere hacer

print("Suma:", suma) #Se muestra el resultado de la Suma de las variables asignadas en numero1 y numero2
print("Resta:", resta) #Se muestra el resultado de la Resta de las variables asignadas en numero1 y numero2
print("Multiplicación:", multiplicacion) #Se muestra el resultado de la Multiplicación de las variables asignadas en numero1 y numero2
print("División:", division) #Se muestra el resultado de la División de las variables asignadas en numero1 y numero2

# En esta ultima sección se muestran al usuario los resultados de las operaciones elaboradas.

